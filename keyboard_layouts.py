def get_layout_dict(layout_name: str):
    """
    Возвращает пару словарей (forward, reverse) для указанной раскладки

    Args:
        layout_name (str): Название раскладки ('en_ru', 'ru_en', и т.д.)

    Returns:
        tuple: (forward_dict, reverse_dict)

    Raises:
        ValueError: Если раскладка не найдена
    """
    layout_pairs = {
        "en_ru": (en_ru, ru_en),
        "ru_en": (ru_en, en_ru),
        # Здесь можно добавить другие пары раскладок
        # 'en_ua': (en_ua, ua_en),
        # 'ua_en': (ua_en, en_ua),
    }

    if layout_name not in layout_pairs:
        raise ValueError(
            f"Раскладка '{layout_name}' не найдена. Доступные: {list(layout_pairs.keys())}"
        )

    return layout_pairs[layout_name]


